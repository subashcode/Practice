// Unary Operators - Incrementnting and Decrementing 
x = 10;
x = x - 1; // Decreses value og x by 1 
x = x + 10; // Increases value of x by 10.
--x; // Decreases value of x by 1
++x; // Increases value of x by 1

var x = 5;
var y = 10;
y = y + x; // assigns the value 15 to the variable y
